    /**
     * Handles the requesting of the camera permission.  This includes
     * showing a "Snackbar" message of why the permission is needed then
     * sending the request.
     */
    private void requestCameraPermission() {
        Log.w(TAG, "Camera permission is not granted. Requesting permission");

        final String[] permissions = new String[]{Manifest.permission.CAMERA};

        if (!ActivityCompat.shouldShowRequestPermissionRationale(this.loader, Manifest.permission.CAMERA))
            ActivityCompat.requestPermissions(this.loader, permissions, RC_HANDLE_CAMERA_PERM);
    }

    /**
     * Generic tracker which is used for tracking or reading a barcode (and can really be used for
     * any type of item).  This is used to receive newly detected items, add a graphical representation
     * to an overlay, update the graphics as the item changes, and remove the graphics when the item
     * goes away.
     */
    class BarcodeTracker extends Tracker<Barcode> {
        @Override
        public void onNewItem(int id, Barcode item) {
            Log.i(TAG, "new BarCode:"+item.displayValue);
            Handler mainHandler = new Handler(BarCodeReader.this.loader.getMainLooper());
            final JSONArray results = new JSONArray();
            results.put(item.displayValue);
            Runnable myRunnable = new Runnable() {
                @Override
                public void run() {
                    apiCall.sendCallbackWithResult("callback", results);
                }
            };
            mainHandler.post(myRunnable);
        }
    }

